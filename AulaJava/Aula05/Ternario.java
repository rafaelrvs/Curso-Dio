package Aula05;

public class Ternario {
  public static void main(String[] args) {
    // Operadores com condição ternaria é
    // uma forma de resumir e escolher por dentro de dois valores
    // É representado por ? caso sejá :

    int a, b;
    a = 5;
    b = 5;
    String resultado = a == b ? "verdadeiro" : "falso"; 
    System.out.println(resultado);

  }
}
